class Solution{   
public:
    long maximumSumSubarray(int K, vector<int> &arr , int N) {
        long result = INT_MIN;
        long sum = 0;
        for(int left = 0, right = 0; right < N; right++) {
            sum += arr[right];
            if(right - left + 1 == K) {
                result = max(result, sum);
                sum -= arr[left];
                left++;
            }
        }
        return result;
    }
};

